//测试获取值的方法， 说明两种定义似乎没有区别
var test = {test1: 1, "test2": 2};
print(test.test1, test["test1"])
print(test["test2"], test.test2)

//增加与删除要素
var test = {}
print("没添加: ", test.test1)
test.test1 = "hello"
print("已添加: ", test.test1)
delete(test.test1)
print("已删除:", test.test1)
test.test1 = null
print("空值:", test.test1)

//判断是否存在该属性
print("是否存在? :","test1" in test)

